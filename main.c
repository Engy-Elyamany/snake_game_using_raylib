#include "snake_game.h"
int main()
{
    InitWindow(screen_width, screen_height, "Snake");
    SetTargetFPS(60);

    init_game();

    while (!WindowShouldClose())
    {
        // ---------------------------------------------------------------------------------------------------------
        // Update
        // ---------------------------------------------------------------------------------------------------------
        snake_movement();
        food_randomization();
        collision_logic();

        // ---------------------------------------------------------------------------------------------------------
        // Drawing
        // ---------------------------------------------------------------------------------------------------------

        draw_game();
    }

    CloseWindow();
}
